package br.com.generation.exercicios1711;

public abstract class Animal {
	
	public void nome(){
		System.out.println("Nome de um animal: nome");
	}
	public void idade() {
		System.out.println("Idade de um animal: idade");
	}
	public void somE() {
		System.out.println("Som emitido por um animal: som");
	}
	public void movimento() {
		System.out.println("Movimentação do animal: movimento");
	}

}
